    <h3>in Italy</h3>
    <div class="lines"></div>
    <p>Florence, the cradle of the Renaissance, is a city rich in history, art, and architecture. Visitors flock to the iconic Florence Cathedral with its magnificent dome engineered by Brunelleschi. The Uffizi Gallery houses an extensive collection of Renaissance art, including works by Michelangelo, Leonardo da Vinci, and Botticelli. Stroll across the Ponte Vecchio, a medieval stone bridge lined with jewelry shops, and enjoy the scenic views of the Arno River.</p>
